package model;

/**
 * is defining a new enumeration type called `Category`. An enumeration is a
 * special type in Java that represents a fixed set of constants. In this
 * case, the constants are the different categories that a product can belong
 * to.
 */
public enum Category {
    BOOKS, ELECTRONIC, CLOTHING_AND_ACCESORIES,
    FOOD_AND_DRINK, STATIONERY, SPORTS,
    BEAUTY_AND_PERSONAL_CARE, TOYS_AND_GAMES
}
